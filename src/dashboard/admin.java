    public void loadDashboardStats() {
        javax.swing.table.DefaultTableModel model
                = (javax.swing.table.DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);

        int totalOrders = 0;
        double totalRevenue = 0;
        int totalCustomers = 0;
        int totalBooks = 0;

        try (java.sql.Connection conn = config.config.connectDB()) {

            // Total Orders
            try (java.sql.PreparedStatement pst = conn.prepareStatement(
                    "SELECT COUNT(*) AS total FROM tbl_orders");
                    java.sql.ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    totalOrders = rs.getInt("total");
                }
            }

            // Total Revenue
            try (java.sql.PreparedStatement pst = conn.prepareStatement(
                    "SELECT SUM(o_total) AS total FROM tbl_orders");
                    java.sql.ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    totalRevenue = rs.getDouble("total");
                }
            }

            // Total Customers
            try (java.sql.PreparedStatement pst = conn.prepareStatement(
                    "SELECT COUNT(*) AS total FROM tbl_accounts WHERE type = 'user'");
                    java.sql.ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    totalCustomers = rs.getInt("total");
                }
            }

            // Total Books
            try (java.sql.PreparedStatement pst = conn.prepareStatement(
                    "SELECT COUNT(*) AS total FROM tbl_books");
                    java.sql.ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    totalBooks = rs.getInt("total");
                }
            }

            // ✅ Add as single row
            model.addRow(new Object[]{
                totalOrders + " orders",
                "₱" + String.format("%.2f", totalRevenue),
                totalCustomers + " users",
                totalBooks + " books"
            });

            // ✅ Style the table
            jTable3.setRowHeight(50);
            jTable3.setFont(new java.awt.Font("Tahoma", java.awt.Font.BOLD, 16));
            jTable3.getTableHeader().setFont(new java.awt.Font("Tahoma", java.awt.Font.BOLD, 13));
            jTable3.setBackground(new java.awt.Color(204, 255, 204));

        } catch (Exception e) {
            System.out.println("Dashboard Stats Error: " + e.getMessage());
        }
    }

    public void displayTopSelling() {
        javax.swing.table.DefaultTableModel model
                = (javax.swing.table.DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        // ✅ SUM quantities from tbl_order_items, grouped by book name
        String sql = "SELECT b_name, SUM(oi_qty) AS total_sold "
                + "FROM tbl_order_items "
                + "GROUP BY b_name "
                + "ORDER BY total_sold DESC";

        try (java.sql.Connection conn = config.config.connectDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                java.sql.ResultSet rs = pst.executeQuery()) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("b_name"),
                    rs.getInt("total_sold")
                });
            }

        } catch (Exception e) {
            System.out.println("Error Loading Top Selling: " + e.getMessage());
        }
    }

    public void displayOrders() {
    javax.swing.table.DefaultTableModel model
            = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    // ✅ LEFT JOIN so orders still show even if user was deleted
    String sql = "SELECT o.o_id, a.fn || ' ' || a.ln AS customer, "
            + "o.o_date, o.o_total "
            + "FROM tbl_orders o "
            + "LEFT JOIN tbl_accounts a ON o.u_id = a.id "
            + "ORDER BY o.o_date DESC";

    try (java.sql.Connection conn = config.config.connectDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery()) {

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("o_id"),
                rs.getString("customer") != null ? rs.getString("customer") : "Unknown",
                rs.getString("o_date"),
                "₱" + rs.getString("o_total")
            });
        }
        System.out.println("Orders loaded: " + model.getRowCount());
    } catch (Exception e) {
        System.out.println("Error Loading Orders: " + e.getMessage());
        javax.swing.JOptionPane.showMessageDialog(this, "Order Error: " + e.getMessage());
    }
}

