 public void loadUsers() {
    javax.swing.table.DefaultTableModel model = 
        (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    try (java.sql.Connection conn = config.config.connectDB();
         java.sql.Statement stmt = conn.createStatement();
         java.sql.ResultSet rs = stmt.executeQuery(
             "SELECT id, fn, ln, email, status FROM tbl_accounts WHERE type = 'user'")) {

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id"),
                rs.getString("fn"),
                rs.getString("ln"),
                rs.getString("email"),
                rs.getString("status")  // added this
            });
        }

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading users: " + e.getMessage());
    }
}

