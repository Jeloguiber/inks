    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a user first!");
        return;
    }

    String id = jTable1.getValueAt(row, 0).toString();

    try (java.sql.Connection conn = config.config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "UPDATE tbl_accounts SET status = 'approved' WHERE id = ?")) {

        pst.setString(1, id);
        pst.executeUpdate();

        javax.swing.JOptionPane.showMessageDialog(this, "User Approved!");
        loadUsers();

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            // TODO add your handling code here:
            int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a user first!");
        return;
    }

    String id = jTable1.getValueAt(row, 0).toString();

    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to decline this user?",
        "Confirm",
        javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try (java.sql.Connection conn = config.config.connectDB();
             java.sql.PreparedStatement pst = conn.prepareStatement(
                 "UPDATE tbl_accounts SET status = 'declined' WHERE id = ?")) {

            pst.setString(1, id);
            pst.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this, "User Declined!");
            loadUsers();

        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

